package com.StringsAndStringBuilder;

public class PrettyPrinting {
    public static void main(String[] args) {
        float num = 457.2594f;

        System.out.printf("The formatted number is: %.2f",  num);
        System.out.println();
        System.out.printf("The formatted Value of PI is: %.2f", Math.PI);
        System.out.println();
        System.out.printf("Hello my name is %s and I am %s", "Vishu", "Curious");


        // The %s or %f are called format specifiers, there are various types of format Specifiers
        // Search on the internet.
    }
}
